// sqrt

let sqrt = Math.sqrt(144);
console.log(`Square root of 144 is: ${sqrt}`);

//pow
let pow = Math.pow(2,5);
console.log(`2 ^ of 5 is: ${pow}`);

// min
let min = Math.min(1,255,69,5966,66,66,6,9,896);
console.log(`Min is ${min}`);

// max
let max = Math.max(1,255,69,5966,66,66,6,9,896);
console.log(`Max is ${max}`);

//random
let random = Math.random();
console.log(random);

//round
random = Math.round(Math.random() * 10000);
